export const HoverEffect = ({
  items,
  className
}) => {
  let [hoveredIndex, setHoveredIndex] = useState(null);
  let [isOpenModal, setOpenModal] = useState(false);
  const hiddenButtonRef = useRef(null);

  let scrollY = 0;

  function openModal() {
    scrollY = window.scrollY;
    document.body.style.position = "fixed";
    document.body.style.top = `-${scrollY}px`;
    document.body.style.width = "100%";
  }

  function closeModal() {
    document.body.style.position = "";
    document.body.style.top = "";
    window.scrollTo(0, scrollY); // Restore scroll position
  }

  const handleClickPopUp = () => {

    if (hiddenButtonRef.current) {
      hiddenButtonRef.current.click();
    }
    console.log("hello")
    console.log(hiddenButtonRef.current)

  }


  return (
    (<div
      className={cn("grid grid-cols-1 md:grid-cols-2  lg:grid-cols-3  py-10", className)}>
      {items.map((item, idx) => (
        <div
          key={idx}
          className="relative group  block p-2 h-full w-full"
          onMouseEnter={() => setHoveredIndex(idx)}
          onMouseLeave={() => setHoveredIndex(null)}
          onClick={handleClickPopUp}
        >
          {/* <AnimatedModalDemo  data={item} refference={hiddenButtonRef} /> */}
          <AnimatePresence>
            {hoveredIndex === idx && (
              <motion.span
                className="absolute inset-0 h-full w-full bg-gradient-to-r from-purple-600 to-[#00d2ff] dark:bg-slate-800/[0.8] block  rounded-3xl"
                layoutId="hoverBackground"
                initial={{ opacity: 0 }}
                animate={{
                  opacity: 1,
                  transition: { duration: 0.15 },
                }}
                exit={{
                  opacity: 0,
                  transition: { duration: 0.15, delay: 0.2 },
                }} />
            )}
          </AnimatePresence>
          <Card className={"relative"} >
            <CardTitle>{item.title}</CardTitle>
            <CardDescription>{item.short_description}</CardDescription>
            <div className=" mt-5 w-full flex justify-between">
              <Link href={item.gitHubUrl} className="py-1 px-2 bg-neutral-950 text-sm shadow-2xl hover:shadow-[4px_4px_0px_theme(colors.neutral.600)]">GitHub &#8599;</Link>
              <Link href={item.host_serverUrl} className="py-1 px-2 bg-neutral-950 text-sm shadow-2xl hover:shadow-[4px_4px_0px_theme(colors.neutral.600)]">Open Website &#8599;</Link>
            </div>
          </Card>
        </div>
      ))}
    </div>)
  );
};

